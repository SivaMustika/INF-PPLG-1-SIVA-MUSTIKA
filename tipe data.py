# Contoh penggunaan tipe data di Python

# Tipe data dasar
angka = 10                   # Integer
pecahan = 3.14               # Float
teks = "Halo, dunia!"       # String
boolean = True              # Boolean

# Menampilkan tipe data
print("Tipe dari angka:", type(angka))
print("Tipe dari pecahan:", type(pecahan))
print("Tipe dari teks:", type(teks))
print("Tipe dari boolean:", type(boolean))

# Operasi sederhana
print("Penjumlahan:", angka + 5)
print("Gabungan teks:", teks + " Apa kabar?")
print("Negasi boolean:", not boolean)
