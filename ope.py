# Operator Aritmatika
a = 10
b = 3

print("Penjumlahan:", a + b)
print("Pengurangan:", a - b)
print("Perkalian:", a * b)
print("Pembagian:", a / b)
print("Modulus (sisa bagi):", a % b)
print("Pangkat:", a ** b)
print("Pembagian bulat:", a // b)

# Operator Perbandingan
print("Apakah a sama dengan b?", a == b)
print("Apakah a tidak sama dengan b?", a != b)
print("Apakah a lebih besar dari b?", a > b)
print("Apakah a lebih kecil atau sama dengan b?", a <= b)

# Operator Logika
x = True
y = False

print("x AND y:", x and y)
print("x OR y:", x or y)
print("NOT x:", not x)