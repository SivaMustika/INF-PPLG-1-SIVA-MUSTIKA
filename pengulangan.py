# WHILE LOOP
print("Contoh WHILE LOOP:")
i = 1
while i <= 5:
    print("Siva")
    i += 1

# FOR LOOP
print("\nContoh FOR LOOP:")
for i in range(5):
    print("Siva")

# NESTED LOOP
print("\nContoh NESTED LOOP:")
for i in range(3):  # Baris
    for j in range(3):  # Kolom
        print("Siva", end=" ")
    print()  # pindah ke baris baru
